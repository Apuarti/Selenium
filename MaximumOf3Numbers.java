package firstjava;





public class MaximumOf3Numbers {

	public static void main(String[] args) {
		int a=10, b=20, c=30;
		
		if (a>b && a>c) {
			System.out.println("a is big number");
			
		}
		else if(b>a && b>c) {
			System.out.println("b is big number");
			
		}
		else {
			System.out.println("c is big number");
			
		}
	}

}

